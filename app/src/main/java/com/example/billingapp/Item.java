public class Item {

    String code = null;
    String name = null;
    String number = null;
    String price = null;

    public String getCode() {
        return code;
    }
    public void setCode(String code) {
        this.code = code;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getNumber() {
        return number;
    }
    public void setNumber(String number) {
        this.number = number;
    }
    public String getPrice() {
        return price;
    }
    public void setRegion(String price) {
        this.price = price;
    }


}

